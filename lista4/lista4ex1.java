package lista4;
public class lista4ex1 {
    public static void main(String[] args) {
        int[] X = new int[10]; 
        for (int i = 0; i < X.length; i++) {
            X[i] = 30;
        }
        System.out.print("Vetor X: ");
        for (int i = 0; i < X.length; i++) {
            System.out.print(X[i] + " ");
        }
    }
} 

